//Aca manejamos todas las operaciones
public class Calculadora {
    
    public static long sumar(long entero1, long entero2){ return entero1 + entero2;}   
    public static long dividir (long entero1, long entero2){return entero1 / entero2;}    
    public static long multiplicar (long entero1, long entero2){return entero1 * entero2;}
    public static long restar (long entero1, long entero2){return entero1 - entero2;}
        
}
